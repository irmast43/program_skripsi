     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $id_user = Auth::user()->id;
        $data = User::where('id', $id_user)->first();

        if($request->file('file_foto')){
            $r = $request->file('file_foto');
            $fileName = time()."_".$r->getClientOriginalName();
            $r->move(public_path().'/foto-profile', $fileName);
        }

        User::where('id', $id_user)->update([
            'name'  => $request->name,
            'email' => $request->email,
            'password' => !empty($request->password) ? Hash::make($request->password) : $data->password,  
            'file'  => isset($request->file_foto) ? $fileName : $data->file
        ]);
        toastr()->success('Profile berhasil diperbarui');
        return redirect()->back();
    }

